        <ol>
          <li>The Hearing Committee will be comprised of six faculty members chosen by the Senate Agenda Committee from among its members and/or the members of the previous four Agenda Committees.</li>
          <li>A master will be appointed to facilitate the hearing process as an agent of the Hearing Committee. The master will be a legal professional with experience as an arbitrator, chosen by the Hearing Committee.</li>
