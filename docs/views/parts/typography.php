        <h3>Hearing Proceedings</h3>
        <ol>
          <li>Initial Hearing Session: The initial session of the hearing will take place on the date established by the Agenda Committee in the notice of hearing. A postponement may be granted only at the will of the Hearing Committee. This session will bring together the parties, the Hearing Committee and the master, to place in the record the statement of charges and to deal with preliminary procedural matters. &nbsp;The session will be convened by the Chair of the Hearing Committee. After having been introduced to the parties, the master will then preside. &nbsp;Upon adjournment of the initial session, the formal hearing before the Committee will normally resume only after the master has completed the findings of fact.</li>
          <li>Fact Finding: To facilitate the progress of the formal hearing, the Hearing Committee will designate the master to be its agent in determining the facts of the case. The master will hold evidentiary sessions in which the administration and the faculty member will present the evidence in the case, including testimony and cross-examination of witnesses. A verbatim transcript will be taken.</li>
          <li>Hearing Committee members will not be required to attend these sessions.When the presentation of evidence is concluded, the master will make the findings of fact. These findings shall be presented to the Hearing Committee, in writing. The University and the faculty member may present specific written exceptions to the findings of fact if they believe that a finding is not reasonably based on evidence in the record. &nbsp;The transcript of the presentation of evidence will be made available to the Hearing Committee.</li>
        </ol>
        <h3>Procedures</h3>
        <ol>
          <li>The Hearing Committee and the master will not be bound by strict legal rules of evidence, and may admit any evidence which has a tendency to prove any fact of consequence to a determination of the issues to be decided relative to the rules, regulations, policies, and/or norms of professional conduct alleged to have been violated.<br>
          Unless special circumstances warrant, it should not be necessary to follow formal rules of court procedure. The Hearing Committee, in consultation with the master, will make rulings with regard to granting adjournment or termination of the hearing.</li>
          <li>The proceedings shall be closed unless all parties including the Hearing Committee agree that they should be open.</li>
          <li>Consistent with this module, the faculty member will be afforded an opportunity to obtain necessary witnesses and documentary or other evidence. To this end, the administration will cooperate with the Hearing Committee and the master in attempting to secure witnesses and making available documentary and other evidence. The master shall provide a schedule for the notice of witnesses and other evidence. Unreasonable or unexplained failure to adhere to the schedule, as determined by the master, risks forfeiture of availability to present witnesses and other evidence.</li>
          <li>The faculty member and the administration will have the right to confront and cross-examine all witnesses unless the faculty member has been excluded from doing so under the terms of II.B. and II.C. above. Where witnesses cannot or will not appear, but the master or the Hearing Committee determines that the interests of justice require admission of their statements, the witnesses will be identified; the master will conduct an interview, if possible, and will read a report of the interview into the record.</li>
          <li>In the hearing on the bill of particulars, the testimony may include that of experts including qualified faculty members from this or other colleges or universities.</li>
          <li>A verbatim transcript of the fact-finding sessions and of the hearing will be taken. Once the transcript has been received from the stenographer, a copy will be made available to the faculty member at his/her request.</li>
        </ol>
        <h3>Formal Hearing</h3>
        <ol>
          <li>When the formal hearing resumes, the Hearing Committee will receive the master’s findings and any exceptions thereto, and will hear oral argument by both sides.</li>
          <li>If circumstances warrant, the Hearing Committee may request documentary evidence or the appearance of witnesses other than those presented by the faculty member or the administration. The Hearing Committee or the master (at its request) may reexamine witnesses heard during the fact- finding.</li>
          <li>If the Hearing Committee feels that written briefs would be helpful, it may request them.</li>
        </ol>
        <h2>Consideration by Hearing Committee</h2>
        <ol>
          <li>The Hearing Committee will reach its decision in conference without assistance of counsel or the master. The findings of fact and the decision will be based solely on the hearing record. The burden of proof that adequate cause exists rests with the administration and will be satisfied only by clear and convincing evidence in the record considered as a whole.</li>
          <li>The Hearing Committee will make explicit findings with respect to each of the charges presented. It may, at its discretion, prepare a reasoned opinion. If the Hearing Committee decides that the facts establish adequate cause for some form of discipline, but not for dismissal, it may recommend a disciplinary sanction other than dismissal. If a faculty member has previously been suspended without pay, such a suspension may not be recommended by the Hearing Committee.</li>
          <li>Publicity concerning the Hearing Committee’s decision should be withheld until final consideration has been given to the case by the Board of Trustees. Any release to the public should be made through the President’s Office.</li>
        </ol>
        <h2>Consideration by the Board of Trustees</h2>
        <ol>
          <li>The President will transmit to the Board of Trustees the full report of the Hearing Committee, stating its action. If the Board of Trustees, or a duly authorized committee thereof, chooses to review the case, its review will be based on the record of the committee hearing, and it may provide opportunity for argument, oral, written or both, by the principals at the hearing, or by their representatives. The decision of the Hearing Committee should either be sustained or the proceeding returned to the Committee with objections specified. If the Board of Trustees has objections regarding limited aspects of the Hearing Committee decision, the decision may be returned to the Hearing Committee for reconsideration limited to those specified issues.</li>
          <li>The Committee will then reconsider, taking into account the stated objections and receiving new evidence if necessary according to procedures (section V.B. of this module) the Committee provides in writing to the principals from the hearing, or to their representatives. It should frame its decision and communicate it in the same manner as before. The Board of Trustees will make a final decision only after study of the Committee’s reconsideration.</li>
          <li>The Faculty Grievance Procedure shall not apply to this Dismissal Procedure or any final action taken pursuant to it.</li>
        </ol>
        <h2>Suspension of the Faculty Member during Proceedings</h2>
        <p>The University may institute suspension of the faculty member during the proceedings only if immediate harm to the faculty member or to others is threatened by continuing his/her employment responsibilities. Unless legal considerations forbid, any such suspension shall be with pay.</p>
        <h2>Publicity</h2>
        <p>Except for such simple announcements as may be required, covering the time of the hearing and similar matters, public statements about the case by the faculty member, administrative officers, Hearing Committee members, or legal counsel shall be avoided so far as possible until the proceedings have been completed. Announcement of the final decision shall be made by the President. If the final decision differs from the Hearing Committee’s recommendation, the announcement shall include a statement of that recommendation.</p>
        <hr>
        <ol>
          <li class="fs--sm">If a Review Committee under the Sexual Harassment Procedure (adopted by the Board of Trustees, on March 24, 1982 has recommended that the faculty member be dismissed and the President concurs, the President shall request a formal dismissal proceeding without the informal inquiry steps enumerated in this subsection. The Review Committee shall be deemed to have substituted for the Committee of Inquiry, and the Review Committee’s evidence, determination, and summary of reasons shall replace those which would have come from the Committee of Inquiry. In all other respects, the dismissal procedure shall be followed.</li>
        </ol>
